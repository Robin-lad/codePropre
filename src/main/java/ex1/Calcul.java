package ex1;

/**
 * Classe qui réalise des opérations arithmétiques
 * @author DIGINAMIC
 */
public class Calcul {

	/** Méthode qui retourne l'addition des paramètres a et b 
	 * 
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return int 
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/**
	 *  Méthode qui retourne la différence entre le param a et b
	 * @param a parametre a
	 * @param b parametre b
	 * @return int
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
